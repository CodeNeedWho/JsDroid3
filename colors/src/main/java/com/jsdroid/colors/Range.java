package com.jsdroid.colors;

public class Range {
    public int left;
    public int top;
    public int right;
    public int bottom;

    @Override
    public String toString() {
        return "Range{" +
                "left=" + left +
                ", top=" + top +
                ", right=" + right +
                ", bottom=" + bottom +
                '}';
    }
}
